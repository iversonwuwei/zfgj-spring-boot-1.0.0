	/**
	 * sessionString?
	 * 
	 * @param key
	 * @return
	 */
	private String getSessionAttrStr(String key) {
		Object obj = ((ServletRequestAttributes) RequestContextHolder.getRequestAttributes()).getRequest().getSession()
				.getAttribute(key);
		if (obj != null) {
			if (obj instanceof String) {
				return (String) obj;
			} else if (obj instanceof Integer) {
				return String.valueOf(obj);
			} else if (obj instanceof ValuedEnum) {
				return String.valueOf(((ValuedEnum) obj).getValue());
			}
		}

		return "";
	}
	
